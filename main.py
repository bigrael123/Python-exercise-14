three_to_twelve = 3
while(three_to_twelve <=12):
    print(f"Listing numbers from 3 to 12: {three_to_twelve}")
    three_to_twelve+=1
for i in range(3, 13, 1):
    print(f"Listing numbers from 3 to 12: {i}")
